void MyTransduStop(UINT16 rev_fwd)
{
    UINT8   i;
    UINT8   buff[20];
    UINT16  tmp_crc;
    
    
    Wait(350000);  
    
    buff[0] = 0x01;
    buff[1] = 0x09;  //写入单笔数据
    buff[2] = 0x00;  //地址
    buff[3] = 0x00;
    
   
    tmp_crc = CRCCheck(buff, 4);
    
    buff[4] = tmp_crc & 0xFF;   //低位在先
    buff[5] = tmp_crc >> 8;
    
    //ENA_RS485_OUT();  
    for (i = 0; i < 6; i++)
    {
        TRANS_COM_SEND_BYTE(buff[i]);
        Wait(30);
    
    }  
    //Wait(600000); 
    //DIS_RS485_OUT();  
   
  
} 


