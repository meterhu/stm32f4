void VFDStop(UINT16 rev_fwd)
{
    UINT8   i;
    UINT8   buff[20];
    UINT16  tmp_crc;
    
    Wait(400000);  //2008-08-24 
    
    buff[0] = 0x01;
    buff[1] = 0x06;  //写入单笔数据
    buff[2] = 0x20;  //地址
    buff[3] = 0x00;
    
    buff[4] = 0x00;
    if (rev_fwd == FWD_STOP)
    {   
        buff[5] = 0x11;
    }
    else if (rev_fwd == REV_STOP)
    {
        buff[5] = 0x21;
    
    }
    
    tmp_crc = CRCCheck(buff, 6);
    
    buff[6] = tmp_crc & 0xFF;   //低位在先
    buff[7] = tmp_crc >> 8;
    
    //ENA_RS485_OUT();  
    for (i = 0; i < 8; i++)
    {
        TRANS_COM_SEND_BYTE(buff[i]);
        Wait(30);
    
    }  
    //Wait(600000); 
    //DIS_RS485_OUT();  
   
  
} 

