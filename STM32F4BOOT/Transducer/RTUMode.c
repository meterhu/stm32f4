void VFDRun(UINT16 rev_fwd, UINT16 rev_num)
{
    UINT8    i;
    UINT8   buff[20];
    UINT16  tmp_crc;
    
    
    //DelayS(2);
    ///Wait(500000);
    Wait(500000);  //2008-08-24 
    //ENA_RS485_OUT();  
    //写入频率
	buff[0] = 0x01;
	buff[1] = 0x06;  //写入单笔数据
	buff[2] = 0x09;  //地址
	buff[3] = 0x07;
	    
	rev_num *= 10;
	rev_num /= 3;
	        
	buff[4] = rev_num >> 8;
	buff[5] = rev_num & 0xFF;
	    
	tmp_crc = CRCCheck(buff, 6);
	    
	buff[6] = tmp_crc & 0xFF;   //低位在先
	buff[7] = tmp_crc >> 8;
	
	//ENA_RS485_OUT();        
	for (i = 0; i < 8; i++)
	{
  	    TRANS_COM_SEND_BYTE(buff[i]);
	    Wait(30);
	    
	} 
	  
	//Wait(600);    
	//DIS_RS485_OUT(); 
    Wait(500000);//2008-08-24 
    //Wait(600000);  
    
    
  	//ENA_RS485_OUT();  
    buff[0] = 0x01;
    buff[1] = 0x06;  //写入单笔数据
    buff[2] = 0x20;  //地址
    buff[3] = 0x00;  
    
    buff[4] = 0;
       
    if (rev_fwd == FWD_RUN)
    {   
        buff[5] = 0x12;
    }
    else if (rev_fwd == REV_RUN)
    {
        buff[5] = 0x22;
    
    }
    
    tmp_crc = CRCCheck(buff, 6);
    
    buff[6] = tmp_crc & 0xFF;   //低位在先
    buff[7] = tmp_crc >> 8;
    
    
    for (i = 0; i < 8; i++)
    {
        TRANS_COM_SEND_BYTE(buff[i]);
        Wait(30);
    
    }   
    
    //Wait(300000); 
    //Wait(600000); 
    //DIS_RS485_OUT(); 
          
}

